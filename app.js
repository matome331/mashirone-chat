    function highlightQuery(html, query) {
        let result = html;

        // 読み仮名パターンハイライト（フィルタON時）
        if (state.readingFilter) {
            result = result.replace(READING_RE_HIGHLIGHT, '<span class="highlight-reading">$&</span>');
        }

        if (!query.trim()) return result;
        
        const terms = query.trim().split(/\s+/).filter(t => t.length > 0);
        
        for (const term of terms) {
            const escaped = term.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
            const regex = new RegExp(`(${escaped})`, 'gi');
            result = result.replace(regex, '<mark>$1</mark>');
        }
        
        return result;
    }

    function debounce(fn, delay) {
        let timer;
        return function (...args) {
            clearTimeout(timer);
            timer = setTimeout(() => fn.apply(this, args), delay);
        };
    }

    // ===== 詳細表示・シーク制御 =====
    function showVideoDetail(video, messages) {
        state.activeVideoId = video.id;
        state.activeVideoMessages = state.loadedChunks[video.id] || [];

        // 検索結果のスクロール位置を保存（戻る時に復元するため）
        state.savedScrollY = window.scrollY;

        dom.searchSection.style.display = 'none';
        dom.resultsSection.style.display = 'none';
        dom.aboutSection.style.display = 'none';
        dom.detailSection.style.display = 'flex';

        const appContainer = document.querySelector('.app');
        if (appContainer) {
            appContainer.classList.add('detail-mode');
        }

        dom.detailVideoTitle.textContent = video.title;
        let formattedDate = video.date || '';
        if (formattedDate) {
            const parts = formattedDate.split('/');
            if (parts.length === 3) {
                formattedDate = `${parts[0]}年${parts[1]}月${parts[2]}日`;
            }
        }
        dom.detailVideoDate.textContent = formattedDate;

        // iframe を新品に差し替えてから src をセット（履歴汚染を防止）
        replacePlayerIframe(`https://www.youtube.com/embed/${video.id}?enablejsapi=1`);

        dom.videoSearchInput.value = state.query;

        renderVideoDetailComments(messages, state.query);

        window.scrollTo({ top: 0, behavior: 'smooth' });
    }

    function renderVideoDetailComments(messages, query) {
        dom.detailCommentsList.innerHTML = '';
        messages.forEach(msg => {
            const card = createDetailChatCard(msg, query);
            dom.detailCommentsList.appendChild(card);
        });
    }

    function createDetailChatCard(msg, query) {
        const div = document.createElement('div');
        div.className = 'detail-chat-card';
        
        const timeStr = formatTime(msg.t);
        const vid = msg.vid || state.activeVideoId;
        const highlightedText = highlightQuery(replaceEmojis(escapeHtml(msg.m)), query);

        // タイムスタンプをクリックで別タブでYouTubeの該当時間に遷移する仕様に変更
        div.innerHTML = `
            <a class="detail-chat-time" href="https://www.youtube.com/watch?v=${vid}&t=${msg.t}s" target="_blank" rel="noopener">${timeStr}</a>
            <div class="detail-chat-body">
                <div class="detail-chat-message">${highlightedText}</div>
            </div>
        `;

        return div;
    }

    function onVideoSearch() {
        if (!state.activeVideoId) return;

        const q = dom.videoSearchInput.value.trim().toLowerCase();
        const terms = q.split(/\s+/).filter(t => t.length > 0);
        
        // この動画の全チャットメッセージからキーワードで絞り込み
        let filtered = state.activeVideoMessages;
        if (terms.length > 0) {
            filtered = filtered.filter(msg => {
                const text = msg.m.toLowerCase();
                const author = msg.a.toLowerCase();
                return terms.every(term => text.includes(term) || author.includes(term));
            });
        }

        // コメント一覧を再描画
        renderVideoDetailComments(filtered, q);
    }

    function seekPlayerTo(seconds) {
        if (dom.youtubePlayer) {
            // postMessage を用いて外部API不要でシーク制御 & 再生開始
            dom.youtubePlayer.contentWindow.postMessage(JSON.stringify({
                event: 'command',
                func: 'seekTo',
                args: [seconds, true]
            }), 'https://www.youtube.com');
            dom.youtubePlayer.contentWindow.postMessage(JSON.stringify({
                event: 'command',
                func: 'playVideo',
                args: []
            }), 'https://www.youtube.com');
        }
    }

    // iframe 要素を新品に差し替えるヘルパー（src 変更による履歴汚染を完全に防止）
    function replacePlayerIframe(newSrc) {
        const old = dom.youtubePlayer;
        const fresh = document.createElement('iframe');
        fresh.id = old.id;
        fresh.className = old.className;
        fresh.setAttribute('allowfullscreen', '');
        fresh.setAttribute('allow', 'accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture');
        if (newSrc) {
            fresh.src = newSrc;
        }
        old.parentNode.replaceChild(fresh, old);
        dom.youtubePlayer = fresh;
    }

    function hideVideoDetail() {
        if (dom.detailSection.style.display !== 'flex') return;
        dom.detailSection.style.display = 'none';
        // iframe を差し替えて動画を停止（src 変更は履歴を汚染するため使わない）
        replacePlayerIframe();
        dom.searchSection.style.display = '';
        dom.resultsSection.style.display = '';

        const appContainer = document.querySelector('.app');
        if (appContainer) {
            appContainer.classList.remove('detail-mode');
        }
        
        // 検索結果画面のスクロール位置を復元
        if (typeof state.savedScrollY === 'number') {
            // display 変更後にレイアウトが確定してからスクロール
            requestAnimationFrame(() => {
                window.scrollTo({ top: state.savedScrollY, behavior: 'instant' });
                state.savedScrollY = null;
            });
        }
    }

    function checkRoute() {
        const hash = window.location.hash;
        if (hash.startsWith('#video-')) {
            const vid = hash.replace('#video-', '');
            if (!isValidVideoId(vid)) return;
            const video = state.index.find(v => v.id === vid);
            if (video) {
                const groupedItem = state.searchResultsGrouped ? state.searchResultsGrouped.find(g => g.vid === vid) : null;
                const messages = groupedItem ? groupedItem.messages : (state.loadedChunks[vid] || []);
                showVideoDetail(video, messages);
                return;
            }
        }
        hideVideoDetail();
    }

