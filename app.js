    function onLoadMore() {
        const fragment = renderResults(state.searchResultsGrouped, state.displayCount, PAGE_SIZE);
        dom.resultsList.appendChild(fragment);
        state.displayCount += PAGE_SIZE;

        if (state.displayCount >= state.searchResultsGrouped.length) {
            dom.loadMore.style.display = 'none';
        }
    }

    function performSearch() {
        if (!state.query.trim() && !state.readingFilter && !state.uncheckedFilter) {
            showWelcome();
            return;
        }

        const results = search(state.query, state.sortOrder);
        state.searchResults = results;
        state.displayCount = 0;

        // 動画単位でグループ化
        const grouped = [];
        const seenVids = new Set();
        for (const msg of results) {
            if (!seenVids.has(msg.vid)) {
                seenVids.add(msg.vid);
                grouped.push({
                    vid: msg.vid,
                    messages: results.filter(r => r.vid === msg.vid)
                });
            }
        }
        state.searchResultsGrouped = grouped;

        // 結果表示
        dom.welcome.style.display = 'none';
        dom.noResults.style.display = 'none';
        dom.resultsList.style.display = 'flex';
        dom.resultsList.innerHTML = '';

        if (grouped.length === 0) {
            dom.resultsList.style.display = 'none';
            dom.noResults.style.display = 'block';
            dom.loadMore.style.display = 'none';
            dom.resultCount.textContent = '0 件';
            return;
        }

        const fragment = renderResults(grouped, 0, PAGE_SIZE);
        dom.resultsList.appendChild(fragment);
        state.displayCount = Math.min(PAGE_SIZE, grouped.length);

        // もっと見るボタン
        dom.loadMore.style.display = grouped.length > PAGE_SIZE ? 'block' : 'none';

        // 件数表示 (ヒットした総コメント数)
        dom.resultCount.textContent = `${results.length.toLocaleString()} 件`;
    }

