    // ===== フィルターヘルパー =====
    function buildMonthOptions() {
        const months = new Set();
        state.index.forEach(v => {
            if (v.date) {
                const ym = v.date.slice(0, 7); // "YYYY/MM"
                months.add(ym);
            }
        });
        const sorted = [...months].sort().reverse();
        const defaultOpt = '<option value="">全期間</option>';
        const options = sorted.map(ym => {
            const [y, m] = ym.split('/');
            return `<option value="${ym}">${y}年${m}月</option>`;
        }).join('');
        dom.dateMonth.innerHTML = defaultOpt + options;
    }

    async function fetchGodokuDates() {
        try {
            const res = await fetch('https://matome331.github.io/mashirone-godoku/webapp/data.js?v=' + Date.now());
            const text = await res.text();
            // data.js は "const dictionaryData = [...];" 形式なので JSON 部分を抽出
            const match = text.match(/\[[\s\S]*\]/);
            if (match) {
                const data = JSON.parse(match[0]);
                data.forEach(entry => {
                    if (entry.date) {
                        state.godokuDates.add(entry.date);
                    }
                });
                console.log(`誤読まとめ: ${state.godokuDates.size}日分の登録済み日付を取得`);
            }
        } catch (e) {
            console.warn('誤読まとめデータの取得に失敗:', e);
        }
    }

    // ===== データ読み込み =====
    async function loadAllChunks() {
        const total = state.index.length;
        let loaded = 0;
        const BATCH_SIZE = 20; // 同時読み込み数

        showStatus(`チャットデータを読み込み中... (0/${total})`);

        // バッチ単位で並列読み込み
        for (let i = 0; i < state.index.length; i += BATCH_SIZE) {
            const batch = state.index.slice(i, i + BATCH_SIZE);

            const promises = batch.map(async (video) => {
                if (!isValidVideoId(video.id)) {
                    console.warn('Invalid video ID skipped:', video.id);
                    return;
                }
                try {
                    const res = await fetch(`${DATA_BASE}/chunks/${video.id}.json?v=13`);
                    const messages = await res.json();
                    state.loadedChunks[video.id] = messages;

                    // 全メッセージリストに追加（ビデオID付き）
                    for (const msg of messages) {
                        state.allMessages.push({
                            vid: video.id,
                            a: msg.a,
                            m: msg.m,
                            t: msg.t,
                            vr: video.rank || 0
                        });
                    }

                    state.totalMessages += messages.length;
                } catch (e) {
                    console.warn(`チャンク読み込み失敗: ${video.id}`, e);
                }
            });

            await Promise.all(promises);

            loaded += batch.length;
            const pct = (loaded / total * 100).toFixed(0);
            dom.progressFill.style.width = pct + '%';
            dom.statusText.textContent = `チャットデータを読み込み中... (${loaded}/${total})`;
        }

        // デフォルトを降順（新しい順）として全体のメッセージリストをソート
        // 新しい動画から順に、同じ動画内ではチャットの時系列順
        state.allMessages.sort((a, b) => (b.vr - a.vr) || (a.t - b.t));
    }

    // 読み仮名パターンの正規表現（漢字/英字/カタカナ + （ひらがな/カタカナ））
    // 前半にひらがなを含めないことで、ふか(いねむ) 等の誤検出を防ぎ、
    // 「は」等の助詞で区切れるので「英語は未来永劫(えいごう)」が「未来永劫(えいごう)」だけになる
    const READING_RE_TEST = /[A-Za-zａ-ｚＡ-Ｚ\u4e00-\u9fffァ-ヶ]+[（(][ぁ-ゖァ-ヶー]+[）)]/;       // フィルタ判定用（gなし）
    const READING_RE_HIGHLIGHT = /[A-Za-zａ-ｚＡ-Ｚ\u4e00-\u9fffァ-ヶ]+[（(][ぁ-ゖァ-ヶー]+[）)]/g; // ハイライト用（gあり）
    // 誤読検索から除外する括弧内の表現（読み仮名ではないもの）
    const READING_IGNORE = ['ドヤァ', 'ザシュ', 'ボキッ'];

    // ===== 検索 =====
    function search(query, sortOrder) {
        const q = query.trim().toLowerCase();
        
        if (!q && !state.readingFilter && !state.uncheckedFilter) {
            return [];
        }

        let results = state.allMessages;

        // 月フィルタ
        if (state.dateMonth) {
            results = results.filter(msg => {
                const vdate = state.videoDateMap[msg.vid] || '';
                if (!vdate) return false;
                return vdate.slice(0, 7) === state.dateMonth;
            });
        }

        // 未チェックフィルタ（godoku 登録済み日付の配信を除外）
        if (state.uncheckedFilter && state.godokuDates.size > 0) {
            results = results.filter(msg => {
                const vdate = state.videoDateMap[msg.vid] || '';
                return !state.godokuDates.has(vdate);
            });
        }

        // テキスト検索（AND条件）
        if (q) {
            const terms = q.split(/\s+/).filter(t => t.length > 0);
            results = results.filter(msg => {
                const text = msg.m.toLowerCase();
                const author = msg.a.toLowerCase();
                return terms.every(term => text.includes(term) || author.includes(term));
            });
        }

        // 読み仮名パターンフィルタ
        if (state.readingFilter) {
            results = results.filter(msg => {
                if (!READING_RE_TEST.test(msg.m)) return false;
                // 無視リストに含まれるマッチだけの場合は除外
                const matches = msg.m.match(READING_RE_HIGHLIGHT);
                if (!matches) return false;
                return matches.some(m => !READING_IGNORE.some(ig => m.includes(ig)));
            });
        }

        // 日付・時間ソート
        if (sortOrder === 'desc') {
            results.sort((a, b) => (b.vr - a.vr) || (a.t - b.t));
        } else {
            results.sort((a, b) => (a.vr - b.vr) || (a.t - b.t));
        }

        return results;
    }

    // ===== 表示 =====
    function renderResults(groupedResults, startIdx, count) {
        const fragment = document.createDocumentFragment();
        const end = Math.min(startIdx + count, groupedResults.length);
        
        for (let i = startIdx; i < end; i++) {
            const item = groupedResults[i];
            const video = state.index.find(v => v.id === item.vid);
            if (video) {
                const header = createVideoGroupHeader(video, item.messages.length, () => {
                    const newHash = `#video-${video.id}`;
                    if (window.location.hash === newHash) {
                        // 同じハッシュへの再クリック: hashchange は発火しないので直接表示
                        checkRoute();
                    } else {
                        window.location.hash = newHash;
                    }
                });
                fragment.appendChild(header);
            }
        }

        return fragment;
    }

    function createVideoGroupHeader(video, matchCount, onCommentsClick) {
        const div = document.createElement('div');
        div.className = 'video-group-header';
        
        const thumbUrl = `https://i.ytimg.com/vi/${video.id}/mqdefault.jpg`;
        
        let formattedDate = video.date || '';
        if (formattedDate) {
            const parts = formattedDate.split('/');
            if (parts.length === 3) {
                formattedDate = `${parts[0]}年${parts[1]}月${parts[2]}日`;
            }
        }

        div.innerHTML = `
            <div class="video-thumbnail-container">
                <img class="video-thumbnail" src="${thumbUrl}" alt="thumbnail" loading="lazy">
            </div>
            <div class="video-info-container">
                <h3 class="video-title">${escapeHtml(video.title)}</h3>
                <div class="video-meta">
                    <span class="video-date">${formattedDate}</span>
                </div>
                <div class="video-match-count">
                    <button class="match-count-link" type="button">
                        ${matchCount}コメント
                    </button>
                </div>
            </div>
        `;

        const btn = div.querySelector('.match-count-link');
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            onCommentsClick();
        });

        return div;
    }

